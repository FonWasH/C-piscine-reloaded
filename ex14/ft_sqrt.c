int	ft_sqrt(int nb)
{
	int	sqrt;

	if ((nb < 0) || (nb > 2147395600))
		return (0);
	sqrt = 1;
	while ((sqrt * sqrt) < nb)
		sqrt++;
	if ((sqrt * sqrt) == nb)
		return (sqrt);
	else
		return (0);
}
